DECLARE	@CUR_DATE			datetime = GETDATE()
DECLARE	@CUR_USER_ID		bigint = 0
DECLARE @BU_CODE			NVARCHAR(10) = N'CSC';

DECLARE @REASON_TYPE_1 BIGINT;
DECLARE @REASON_TYPE_2 BIGINT;
SELECT @REASON_TYPE_1 = MASTER_ID FROM MASTER_CODE WHERE MASTER_CODE.MASTER_CODE = N'PARTS_GODOWN_TRANS';
SELECT @REASON_TYPE_2 = MASTER_ID FROM MASTER_CODE WHERE MASTER_CODE.MASTER_CODE = N'STOCK_ADJ';

INSERT INTO REASON_CODE(REASON_CODE,REASON_DESC ,REASON_TYPE,BU_CODE ,CREATED_BY,CREATION_DATE,LAST_UPDATED_BY ,LAST_UPDATE_DATE )
	SELECT	N'STOCK_IN',					N'STOCK_IN',					@REASON_TYPE_1,	@BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT	N'STOCK_IN_RETURN',				N'STOCK_IN_RETURN',				@REASON_TYPE_1,	@BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT	N'STOCK_IN_CORRECT',			N'STOCK_IN_CORRECT',			@REASON_TYPE_1,	@BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT	N'STOCK_OUT',					N'STOCK_OUT',					@REASON_TYPE_1,	@BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT	N'JOB_USAGE',					N'JOB_USAGE',					@REASON_TYPE_1,	@BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT  N'STOCK_TAKE',					N'STOCK_TAKE',					@REASON_TYPE_2, @BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT  N'COST_UPDATE',					N'COST_UPDATE',					@REASON_TYPE_2, @BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT  N'STOCK_LOST',					N'STOCK_LOST',					@REASON_TYPE_2, @BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE UNION
	SELECT  N'STOCK_DEFECT',				N'STOCK_DEFECT',				@REASON_TYPE_2, @BU_CODE,@CUR_USER_ID,@CUR_DATE,@CUR_USER_ID,@CUR_DATE;